    for (int i = 0; i < n; ++i)
    {
        cin >> a[i];
        sum += a[i];
        mn = min(mn, a[i]);
        mx = max(mx, a[i]);
        mp[a[i]]++;
    }
    if (mp[mx] < 2)
        mx--;
    if (mx - mn > k)
    {
        cout << "Jerry" << endl;
    }
    else if (sum & 1)
        cout << "Tom" << endl;
